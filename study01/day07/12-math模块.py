# 数学计算相关的模块
import math

# 求阶乘
print(math.factorial(6))

# 向下取整
print(math.floor(4.623))

# 向上取整
print(math.ceil(4.223))

# 幂运算
print(math.pow(2, 3))

# round() 内置函数，四舍五入
print(round(3.5))

# 正弦
print(math.sin(math.pi / 6))  # 弧度

# 余弦
print(math.cos(math.pi / 3))

# 正切
print(math.tan(math.pi / 2))  # 正无穷大

