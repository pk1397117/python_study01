# List (列表)
names = ['张三', '李四', '王五', '赵六']
# Dictionary (字典)
person = {'name': '张三', 'age': '18', 'address': '武汉', '身高': '180cm'}
# Tuple 元组类型
nums = (1, 2, 3, 4, 5)
# 集合类型
x = {9, 'hello', 'hi', 'good', True}

print(type(names), type(person), type(nums), type(x))

